    public void updateUserPassword(JTextField txtU_name, JTextField txtO_pass, JTextField txtn_pass, 
                                JTextField txtc_pass, JTextField txtU_typ) {

    String userName = txtU_name.getText();
    String oldPassword = txtO_pass.getText();
    String newPassword = txtn_pass.getText();
    String confirmPassword = txtc_pass.getText();
    String userType = txtU_typ.getText();

    // Validate that all fields are filled
    if (!userName.isEmpty() && !userType.isEmpty() && !oldPassword.isEmpty() 
        && !newPassword.isEmpty() && !confirmPassword.isEmpty()) {

        // Check password length
        if (newPassword.length() > 6) {

            // Validate that new password matches confirmation password
            if (newPassword.equals(confirmPassword)) {
                JOptionPane.showMessageDialog(null, "Passwords match");

                try {
                    Connection con = db.Database1.getConnection();

                    // Verify the old password
                    String checkQuery = "SELECT password FROM user WHERE user_name = ?";
                    PreparedStatement checkStmt = con.prepareStatement(checkQuery);
                    checkStmt.setString(1, userName);
                    ResultSet rs = checkStmt.executeQuery();

                    if (rs.next()) {
                        String dbOldPassword = rs.getString("password");

                        // Check if the old password is correct
                        if (!dbOldPassword.equals(oldPassword)) {
                            JOptionPane.showMessageDialog(null, "Old password is incorrect.");
                            return;
                        }

                        // Update the password and user type
                        String updateQuery = "UPDATE user SET password = ?, data_type = ? WHERE user_name = ?";
                        PreparedStatement pst = con.prepareStatement(updateQuery);

                        pst.setString(1, newPassword);
                        pst.setString(2, userType);
                        pst.setString(3, userName);

                        int rowsAffected = pst.executeUpdate();

                        if (rowsAffected > 0) {
                            JOptionPane.showMessageDialog(null, "Password updated successfully.");

                            // Clear the input fields after success
                            txtU_name.setText("");
                            txtO_pass.setText("");
                            txtn_pass.setText("");
                            txtc_pass.setText("");
                            txtU_typ.setText("");
                        } else {
                            JOptionPane.showMessageDialog(null, "Failed to update user data.");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "User not found.");
                    }

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
                }

            } else {
                JOptionPane.showMessageDialog(null, "Passwords do not match.");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Password should be more than 6 characters.");
        }

    } else {
        JOptionPane.showMessageDialog(null, "Please fill in all the fields.");
    }
    }

